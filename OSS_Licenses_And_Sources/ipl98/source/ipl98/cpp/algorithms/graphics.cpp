bool CGraphics::AdjustAlignment(const char* str, CPoint2D<int>& Pos)
{
	UINT8* pFontData;
	UINT8 ASCIIStart;
	int TotalChars,CharWidth,CharHeight;
	GetFontInfo(m_FontActive,&pFontData,ASCIIStart,TotalChars,CharWidth,CharHeight);
	switch(m_FontAlignment)
	{
	case TOPLEFT:
		{
			// do nothing
			break;
		}
	case BOTTOMLEFT:
		{
			Pos.SetY(Pos.GetY()-CharHeight);
			break;
		}
	case TOPMID:
		{
			int Size=GetSizeString(str);
			Pos.SetX(Pos.GetX()-Round(Size*CharWidth/2));
			break;
		}
	case BOTTOMMID:
		{
			int Size=GetSizeString(str);
			Pos.SetX(Pos.GetX()-Round(Size*CharWidth/2));
			Pos.SetY(Pos.GetY()-CharHeight);
			break;
		}
	case TOPRIGHT:
		{
			int Size=GetSizeString(str);
			Pos.SetX(Pos.GetX()-Round(Size*CharWidth));
			break;
		}
	case BOTTOMRIGHT:
		{
			int Size=GetSizeString(str);
			Pos.SetX(Pos.GetX()-Round(Size*CharWidth));
			Pos.SetY(Pos.GetY()-CharHeight);
			break;
		}
	default:
		{
			ostringstream ost;
			ost << "CGraphics::AdjustAlignment() Unknown alignment value" << IPLAddFileAndLine;
			CError::ShowMessage(IPL_ERROR,ost);
			return false;
		}
	}
	return true;
}

int CGraphics::GetSizeString(const char* str)
{
	int Size=0;
	const char* s=str;
	while(*s!='\0')
	{
		Size++;
		s++;
	}
	return Size;
}


bool CGraphics::PlotChar(char ch, const CPoint2D<int>& Pos, CStdImage& Img)
{
	int TotalChars;
	int CharWidth;
	int CharHeight;
	UINT8 ASCIIStart;
	UINT8 c=(UINT8)ch;
	UINT8* pFontData=NULL;
	int MinX=Img.GetMinX();
	int MaxX=Img.GetMaxX();
	int MinY=Img.GetMinY();
	int MaxY=Img.GetMaxY();
	if (GetFontInfo(m_FontActive,&pFontData,ASCIIStart,TotalChars,CharWidth,CharHeight)==false)
		return false;
	int x,y,k,Index;
	if ((c<ASCIIStart) || (c>=TotalChars)) c=ASCIIStart; // if character not used, print the lowest available
	c-=ASCIIStart;
	int ScanLength=(CharWidth*(TotalChars-ASCIIStart))/8+1; // in bytes
	int Offset=CharWidth*(int)c;
	int StartIndex=(int)Offset/8;
	int BitOffsetF=Offset % 8;
	// number of whole bytes in width of character to be read after the first part of a byte has been read
	int WholeBytes=(CharWidth-(8-BitOffsetF))/8; 
	int RemainingBits=CharWidth-((8-BitOffsetF)+8*WholeBytes); // 5

	for(y=0; y<CharHeight; y++)
	{
		x=Pos.GetX();
		Index=y*ScanLength+StartIndex;
		// run in part of first byte
		for(k=0;k<8-BitOffsetF;k++)
		{
			UINT8 PlotVal=pFontData[Index];
			PlotVal <<= BitOffsetF + k;
			PlotVal >>= 7;
			if (PlotVal==0)
			{
				if ((x>MinX) && (x<MaxX) && (Pos.GetY()+y>MinY) && (Pos.GetY()+y<MaxY))
					Img.SetPixelFast(x,Pos.GetY()+y,m_FontColor);
			}
			else if (m_FontTransparent==false)
			{
				if ((x>MinX) && (x<MaxX) && (Pos.GetY()+y>MinY) && (Pos.GetY()+y<MaxY))
					Img.SetPixelFast(x,Pos.GetY()+y,m_FontBackgroundColor);
			}
			x++;
		}
		// run in whole bytes inbetween
		for(int i=0; i<WholeBytes; i++)
		{
			Index++;
			for(k=0; k<8; k++)
			{
				UINT8 PlotVal=pFontData[Index];
				PlotVal <<= k;
				PlotVal >>= 7;
				if (PlotVal==0)
				{
					if ((x>MinX) && (x<MaxX) && (Pos.GetY()+y>MinY) && (Pos.GetY()+y<MaxY))
						Img.SetPixelFast(x,Pos.GetY()+y,m_FontColor);
				}
				else if (m_FontTransparent==false)
				{
					if ((x>MinX) && (x<MaxX) && (Pos.GetY()+y>MinY) && (Pos.GetY()+y<MaxY))
						Img.SetPixelFast(x,Pos.GetY()+y,m_FontBackgroundColor);
				}
				x++;
			}
		}
		// run in part of last byte
		Index++;
		for(k=0;k<RemainingBits;k++)
		{
			UINT8 PlotVal=pFontData[Index];
			PlotVal <<= k;
			PlotVal >>= 7;
			if (PlotVal==0)
			{
				if ((x>MinX) && (x<MaxX) && (Pos.GetY()+y>MinY) && (Pos.GetY()+y<MaxY))
					Img.SetPixelFast(x,Pos.GetY()+y,m_FontColor);
			}
			else if (m_FontTransparent==false)
			{
				if ((x>MinX) && (x<MaxX) && (Pos.GetY()+y>MinY) && (Pos.GetY()+y<MaxY))
					Img.SetPixelFast(x,Pos.GetY()+y,m_FontBackgroundColor);
			}
			x++;
		}
	}
	return true;
}


bool CGraphics::InitFont()
{
	if (m_FontData[m_FontActive]!=NULL)
	{
		// allready initialize - do nothing
		return true;
	}
	switch(m_FontActive)
	{
	case CourierBold13x18:
		{
			if (m_FontData[m_FontActive]==NULL)
				InitCourierBold13x18();
			break;
		}
	case Courier8x12:
		{
			if (m_FontData[m_FontActive]==NULL)
				InitCourier8x12();
			break;
		}
	case Terminal12x16:
		{
			if (m_FontData[m_FontActive]==NULL)
				InitTerminal12x16();
			break;
		}
	default:
		{
			ostringstream ost;
			ost << "CGraphics::InitFont() Unknown font is active" << IPLAddFileAndLine;
			CError::ShowMessage(IPL_ERROR,ost);
			return false;
		}
	}
	return true;
}

