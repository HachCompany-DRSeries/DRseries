bool CGraphics::PlotString(const char* str, const CPoint2D<int>& Pos, CStdImage& Img)
{
	return PlotString(str,Pos.GetX(),Pos.GetY(),Img);
}

bool CGraphics::PlotString(const char* str, int x, int y, CStdImage& Img)
{
	if (m_FontData[m_FontActive]==NULL)
	{
		InitFont();
	}
	UINT8* pFontData;
	UINT8 ASCIIStart;
	int TotalChars,CharWidth,CharHeight;
	GetFontInfo(m_FontActive,&pFontData,ASCIIStart,TotalChars,CharWidth,CharHeight);
	CPoint2D<int> P(x,y);
	// adjust P according to alignent value
	if (AdjustAlignment(str,P)==false)
		return false;
	int Size=GetSizeString(str);
	int Count=0;
	while(Count<Size)
	{
		if (PlotChar(*(str+Count),P,Img)==false)
			return false;
		P += CPoint2D<int>(CharWidth,0);
		Count++;
	}
	return true;
}

bool CGraphics::GetFontInfo(BITMAPFONT Font,UINT8** ppFontData, UINT8& ASCIIStart, int& TotalChars,int& CharWidth,int& CharHeight)
{
	*ppFontData=m_FontData[m_FontActive];
	switch(Font)
	{
	case CourierBold13x18:
		{
			ASCIIStart=31;
			TotalChars=256;
			CharWidth=13;
			CharHeight=18;
			break;
		}
	case Terminal12x16:
		{
			ASCIIStart=0;
			TotalChars=256;
			CharWidth=12;
			CharHeight=16;
			break;
		}
	case Courier8x12:
		{
			ASCIIStart=0;
			TotalChars=256;
			CharWidth=8;
			CharHeight=12;
			break;
		}
	default:
		{
			ostringstream ost;
			ost << "CGraphics::PlotChar() Unknown font is active" << IPLAddFileAndLine;
			CError::ShowMessage(IPL_ERROR,ost);
			return false;
		}
	}
	return true;
}

