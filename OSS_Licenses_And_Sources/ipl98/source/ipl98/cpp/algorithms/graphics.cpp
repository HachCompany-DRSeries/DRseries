CGraphics::CGraphics() : m_TotalFonts(3)
{
	m_FontColor=0;
	m_FontTransparent=true;
	m_FontActive=CourierBold13x18;
	m_FontAlignment=TOPLEFT;
	m_FontData.resize(m_TotalFonts);
	for(int i=0;i<m_TotalFonts;i++)
		m_FontData[i]=NULL;
}

CGraphics::CGraphics(UINT32 FontColor, bool FontTransparent, FONTALIGNMENT FontAlignment,
					 BITMAPFONT FontActive) : m_TotalFonts(3)
{
	m_FontColor=FontColor;
	m_FontTransparent=FontTransparent;
	m_FontActive=FontActive;
	m_FontAlignment=FontAlignment;
	m_FontData.resize(m_TotalFonts);
	for(int i=0;i<m_TotalFonts;i++)
		m_FontData[i]=NULL;
}

CGraphics::~CGraphics()
{
	for(int i=0;i<m_TotalFonts;i++)
	{
		if (m_FontData[i]!=NULL)
			free(m_FontData[i]);
	}
}

